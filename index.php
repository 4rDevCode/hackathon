    <h1>¿Qué es AylluTanta?</h1>
    <h2>Prepárate para un sismo: Tu guía de emergencia en una sola app</h2>

    <p>
      Bienvenido a nuestra aplicación de preparación para sismos. Te ofrecemos una herramienta completa
      para ayudarte a manejar la situación después de un sismo de manera organizada y segura.
    </p>
    <h3>Funcionalidades principales:</h3>
    <ul>
      <li>
        <strong>Mochila de emergencia:</strong> Crea un inventario de productos, registra las fechas de vencimiento
        de alimentos y medicinas, y asegúrate de tener todo lo necesario en caso de una emergencia.
      </li>
      <li>
        <strong>Identificación de zonas seguras:</strong> Marca las zonas seguras en tu hogar y en los lugares donde
        suelen estar los miembros de tu familia, para que todos sepan dónde dirigirse en caso de un sismo.
      </li>
      <li>
        <strong>Punto de encuentro familiar:</strong> Establece rutas para llegar a un punto de encuentro fuera de tu
        hogar,
        y registra el tiempo estimado para regresar a casa después de un sismo.
      </li>
      <li>
        <strong>Roles durante un sismo:</strong> Define roles para los miembros de tu familia durante una emergencia,
        incluyendo atención a personas con dificultades de movilidad y cuidado de mascotas, entre otros.
      </li>
      <li>
        <strong>Identificación de centros de ayuda:</strong> Encuentra rápidamente postas médicas, hospitales, bomberos,
        policía, serenazgo y otros centros de ayuda cercanos en caso de una emergencia.
      </li>
    </ul>
    <h3>¡Usa esta aplicación ahora y prepárate para enfrentar un sismo de manera organizada y segura!</h3>

